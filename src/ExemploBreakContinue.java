public class ExemploBreakContinue { 
    public static void main(String[] args) {
        for (int numero = 0; numero <=50; numero ++){
            if (numero == 25) {
                System.out.println("O numero é igual a 25 por isso não pode ser escrito");
                continue;
                
            }
            System.out.println(numero);
        }

        }
        }
    
    